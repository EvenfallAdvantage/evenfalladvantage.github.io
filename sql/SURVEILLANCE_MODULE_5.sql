<h3>What is Cyber Stalking?</h3>
<p>Cyber stalking is the use of the Internet, email, or other electronic communications to stalk, harass, or threaten an individual. It may include false accusations, defamation, monitoring, identity theft, threats, or solicitation.</p>

<h4>Common Cyber Stalking Behaviors:</h4>
<ul>
    <li>Sending threatening or harassing messages</li>
    <li>Monitoring online activities</li>
    <li>Posting false information online</li>
    <li>Identity theft or impersonation</li>
    <li>Doxing (publishing private information)</li>
    <li>Cyberbullying and harassment</li>
    <li>Tracking location through social media</li>
    <li>Installing spyware on devices</li>
</ul>

<h3>Social Media Monitoring & OSINT</h3>
<p>Open Source Intelligence (OSINT) involves gathering information from publicly available sources.</p>

<h4>What Stalkers Can Learn from Social Media:</h4>
<ul>
    <li>Your location and travel patterns</li>
    <li>Daily routines and schedules</li>
    <li>Friends and family connections</li>
    <li>Workplace and school information</li>
    <li>Hobbies and interests</li>
    <li>Relationship status</li>
    <li>Photos revealing home/car details</li>
</ul>

<h4>Privacy Settings Best Practices:</h4>
<ul>
    <li>Set profiles to private</li>
    <li>Limit who can see your posts</li>
    <li>Disable location tagging</li>
    <li>Review tagged photos before posting</li>
    <li>Limit friend/follower lists visibility</li>
    <li>Turn off "last seen" indicators</li>
    <li>Disable read receipts</li>
</ul>

<h3>Spyware and Stalkerware</h3>
<p>Malicious software installed on devices to monitor activities without consent.</p>

<h4>Types of Spyware:</h4>
<ul>
    <li><strong>Keyloggers:</strong> Record keystrokes</li>
    <li><strong>Screen Recorders:</strong> Capture screen activity</li>
    <li><strong>Location Trackers:</strong> Monitor GPS location</li>
    <li><strong>Communication Monitors:</strong> Access texts, calls, emails</li>
    <li><strong>Remote Access Tools:</strong> Control device remotely</li>
</ul>

<h4>Signs Your Device May Be Compromised:</h4>
<ul>
    <li>Unusual battery drain</li>
    <li>Increased data usage</li>
    <li>Device running hot</li>
    <li>Unfamiliar apps installed</li>
    <li>Settings changed without your knowledge</li>
    <li>Strange background noise on calls</li>
    <li>Unexpected reboots</li>
</ul>

<h4>Protection Measures:</h4>
<ul>
    <li>Use strong passwords/biometrics</li>
    <li>Enable two-factor authentication</li>
    <li>Keep software updated</li>
    <li>Install from official app stores only</li>
    <li>Review app permissions regularly</li>
    <li>Use mobile security software</li>
    <li>Factory reset if compromised</li>
</ul>

<h3>Email and Messaging Surveillance</h3>
<h4>Threats:</h4>
<ul>
    <li>Email account compromise</li>
    <li>Message interception</li>
    <li>Phishing attacks</li>
    <li>Forwarding rules set without knowledge</li>
</ul>

<h4>Protection:</h4>
<ul>
    <li>Use encrypted messaging apps</li>
    <li>Enable two-factor authentication</li>
    <li>Check for forwarding rules</li>
    <li>Use unique passwords</li>
    <li>Be cautious with links</li>
</ul>

<h3>Location Tracking Through Apps</h3>
<p>Many apps track location even when not in use.</p>

<h4>Common Tracking Methods:</h4>
<ul>
    <li>GPS tracking apps</li>
    <li>Find My Phone features</li>
    <li>Social media check-ins</li>
    <li>Photo metadata (EXIF data)</li>
    <li>Fitness apps</li>
    <li>Dating apps</li>
</ul>

<h4>Protection:</h4>
<ul>
    <li>Review app location permissions</li>
    <li>Disable location services when not needed</li>
    <li>Remove photo metadata before sharing</li>
    <li>Avoid check-ins on social media</li>
    <li>Use VPN services</li>
</ul>

<h3>Doxing and Online Harassment</h3>
<p>Doxing is publishing private information online with malicious intent.</p>

<h4>Information Often Targeted:</h4>
<ul>
    <li>Home address</li>
    <li>Phone numbers</li>
    <li>Email addresses</li>
    <li>Workplace information</li>
    <li>Family member details</li>
    <li>Financial information</li>
</ul>

<h4>Prevention:</h4>
<ul>
    <li>Use privacy services for domain registration</li>
    <li>Opt out of data broker sites</li>
    <li>Use separate email for public accounts</li>
    <li>Limit personal information online</li>
    <li>Use pseudonyms when possible</li>
</ul>

<h3>Reporting Cyber Stalking</h3>
<h4>Steps to Take:</h4>
<ol>
    <li>Document all incidents (screenshots, dates, times)</li>
    <li>Block the stalker on all platforms</li>
    <li>Report to platform administrators</li>
    <li>File police report</li>
    <li>Contact FBI IC3 for federal crimes</li>
    <li>Seek restraining order if appropriate</li>
    <li>Contact victim support organizations</li>
</ol>

<h3>Digital Hygiene Best Practices</h3>
<ul>
    <li>Use password managers</li>
    <li>Enable encryption</li>
    <li>Regular security audits</li>
    <li>Separate personal/professional accounts</li>
    <li>Be cautious about oversharing</li>
    <li>Review privacy policies</li>
    <li>Use secure browsers</li>
</ul>

